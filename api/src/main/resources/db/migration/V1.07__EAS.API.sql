CREATE TABLE ASSESSMENT_SESSION_CRITERIA
(
    ASSESSMENT_SESSION_CRITERIA_ID UUID PRIMARY KEY,
    SESSION_START_MONTH VARCHAR(2) NOT NULL,
    SESSION_START TIMESTAMP NOT NULL,
    SESSION_END TIMESTAMP NOT NULL,
    EFFECTIVE_DATE TIMESTAMP NOT NULL,
    EXPIRY_DATE TIMESTAMP NOT NULL,
    CREATE_USER VARCHAR(32) NOT NULL,
    CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATE_USER VARCHAR(32) NOT NULL,
    UPDATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
)

CREATE TABLE ASSESSMENT_SESSION_TYPE_CODE_CRITERIA
(
    ASSESSMENT_SESSION_TYPE_CODE_CRITERIA_ID UUID PRIMARY KEY,
    ASSESSMENT_TYPE_CODE VARCHAR(10) REFERENCES ASSESSMENT_TYPE_CODE (ASSESSMENT_TYPE_CODE) NOT NULL,
    ASSESSMENT_SESSION_CRITERIA_ID UUID REFERENCES ASSESSMENT_SESSION_CRITERIA (ASSESSMENT_SESSION_CRITERIA_ID) NOT NULL,
    EFFECTIVE_DATE TIMESTAMP NOT NULL,
    EXPIRY_DATE TIMESTAMP NOT NULL,
    CREATE_USER VARCHAR(32) NOT NULL,
    CREATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
    UPDATE_USER VARCHAR(32) NOT NULL,
    UPDATE_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
);

